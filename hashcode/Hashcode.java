package hashcode;

public class Hashcode {

	public static void main(String[] args) {
		Integer j = 10;
		Integer i = 10;
		
		System.out.println("Hashcode J: " + j.hashCode());
		System.out.println("Hashcode I: " + i.hashCode());
		
		String a = "a";
		String z = "z";
		
		System.out.println("Hashcode A: " + a.hashCode());
		System.out.println("Hashcode Z: " + z.hashCode());

	}

}
